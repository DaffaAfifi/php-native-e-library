// Fungsi untuk menghitung total dari tabel
function countTotal($conn, $table, $condition = '') {
  $sql = "SELECT COUNT(*) as total FROM $table $condition";
  $result = mysqli_query($conn, $sql);
  $row = mysqli_fetch_assoc($result);
  return $row['total'];
}

